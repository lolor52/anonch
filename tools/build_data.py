def similarity(left: str, right: str) -> int:
    return sum(a == b for a, b in zip(left, right))


def base_score(left: str, right: str, group_map: dict[str, str]) -> int:
    if left == right:
        return 5

    score = 4
    overlap = similarity(left, right)
    if overlap >= 3:
        score += 1
    elif overlap == 1:
        score -= 1
    elif overlap == 0:
        score -= 2

    if left[1] == right[1]:
        score += 1
    if left[2] == right[2]:
        score += 1
    if left[3] != right[3]:
        score += 1
    if group_map[left] == group_map[right]:
        score += 1

    return max(1, min(7, score))


def build_matrix(type_map: dict[str, dict], strong_notes: dict[str, str], difficult_notes: dict[str, str]) -> dict[str, dict[str, int]]:
    group_map = {code: data["group"] for code, data in type_map.items()}
    matrix: dict[str, dict[str, int]] = {}

    for row in ORDER:
        matrix[row] = {}
        for col in ORDER:
            key = pair_key(row, col)
            if row == col:
                score = 5
            elif key in strong_notes:
                score = 7
            elif key in difficult_notes:
                score = 2
            else:
                score = base_score(row, col, group_map)
            matrix[row][col] = score

    return matrix


def build_highlights(matrix: dict[str, dict[str, int]]) -> dict[str, dict[str, list[str]]]:
    highlights: dict[str, dict[str, list[str]]] = {}

    for code in ORDER:
        peers = [other for other in ORDER if other != code]
        highlights[code] = {
            "strong": sorted(peers, key=lambda other: (-matrix[code][other], other))[:3],
            "difficult": sorted(peers, key=lambda other: (matrix[code][other], other))[:3],
        }

    return highlights


def build_pair_notes(
    type_map: dict[str, dict],
    highlights: dict[str, dict[str, list[str]]],
    strong_notes: dict[str, str],
    difficult_notes: dict[str, str],
) -> dict[str, dict[str, str]]:
    pair_notes = {
        **{key: {"tone": "strong", "text": text} for key, text in strong_notes.items()},
        **{key: {"tone": "difficult", "text": text} for key, text in difficult_notes.items()},
    }

    for code in ORDER:
        for other in highlights[code]["strong"]:
            key = pair_key(code, other)
            pair_notes.setdefault(key, {"tone": "strong", "text": build_auto_pair_note(type_map, code, other, "strong")})

        for other in highlights[code]["difficult"]:
            key = pair_key(code, other)
            pair_notes.setdefault(
                key,
                {"tone": "difficult", "text": build_auto_pair_note(type_map, code, other, "difficult")},
            )

    return pair_notes


def build_auto_pair_note(type_map: dict[str, dict], left: str, right: str, tone: str) -> str:
    left_group = type_map[left]["group"]
    right_group = type_map[right]["group"]

    if tone == "strong":
        if left_group == right_group:
            return f"{left} и {right} обычно быстро сходятся по темпу и легче понимают приоритеты друг друга."
        return f"{left} и {right} часто хорошо дополняют друг друга и закрывают слабые места партнёра."

    if left_group == right_group:
        return f"{left} и {right} похожи по сильным сторонам, но именно поэтому жёстче сталкиваются в спорных местах."
    return f"У {left} и {right} часто расходятся ритм, ожидания и способ договариваться."


